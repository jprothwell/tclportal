/*****************************************************************************
 * FUNCTION
 *  change_font
 * DESCRIPTION
 *  
 * PARAMETERS
 *  void
 * RETURNS
 *  void
 *****************************************************************************/
void change_font(void)
{
    /*----------------------------------------------------------------*/
    /* Local Variables                                                */
    /*----------------------------------------------------------------*/
    U8 fontSize;

    /*----------------------------------------------------------------*/
    /* Code Body                                                      */
    /*----------------------------------------------------------------*/
    fontSize = PhnsetGetFontSize();

    switch (fontSize)
    {
        case THEME_DEFAULT:
            MMI_default_font.size = MEDIUM_FONT;
            break;
        case SMALL:
            MMI_default_font.size = SMALL_FONT;
            break;

        case MEDIUM:
            MMI_default_font.size = MEDIUM_FONT;
            break;
        case LARGE:
            MMI_default_font.size = LARGE_FONT;
            break;

        default:
            MMI_default_font.size = MEDIUM_FONT;
            break;
    }
}


